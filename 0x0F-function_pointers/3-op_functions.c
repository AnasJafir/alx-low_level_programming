#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: integer input
 * @b: integer input
 *
 * Return: the sum of the two integers
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract an integer from an integer
 * @a: integer input
 * @b: integer input
 *
 * Return: the difference between the two integers
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integers
 * @a: integer input
 * @b: integer input
 *
 * Return: the product of multiplying the two integers
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integers
 * @a: integer input
 * @b: integer input
 *
 * Return: the quotient of dividing the two integers
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo two integers
 * @a: integer input
 * @b: integer input
 *
 * Return: the remainder of dividing the two integers
*/
int op_mod(int a, int b)
{
	return (a % b);
}
